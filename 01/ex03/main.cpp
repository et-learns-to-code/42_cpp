#include "Weapon.hpp"
#include "HumanA.hpp"
#include "HumanB.hpp"

// The {} blocks create local scopes, and any variables (like club and bob
// in the first block) are automatically destroyed when the scope ends.
// Thatâ€™s why HumanA (Bob) is destroyed right after the first block concludes,
// and then HumanB (Jim) is created fresh in the second block.
int main()
{
	{
		Weapon club = Weapon("crude spiked club");
		HumanA bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}
	{
		Weapon club = Weapon("crude spiked club");
		HumanB jim("Jim");
		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}
	return 0;
}
